# This file will show you how to use variables in python

# Variables are used to store data
# In python, you don't have to declare the type of a variable
# You can declare a variable and assign a value to it in the same line
# For example:
x = 5
y = "Hello, World!"
z = 5.0

# You can also assign multiple variables in one line
a, b, c = 5, "Hello, World!", 5.0

# now lets print them all out 
print(x)
print(y)
print(z)

print(a)
print(b)
print(c)